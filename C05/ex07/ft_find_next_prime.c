int	ft_is_prime(int n)
{
	int	ispri;

	ispri = 2;
	if (n == 2)
		return (1);
	if (n == 0 || n == 1 || n < 0 || n % 2 == 0)
		return (0);
	while (ispri < n / ispri)
	{
		if (n % ispri == 0)
			return (0);
		ispri++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	if (nb <= 2)
		return (2);
	while (ft_is_prime(nb) == 0)
		nb++;
	return (nb);
}
